ssn_uk_UA = R6Class(
  "ssn_uk_UA",
  inherit = ssn_init,
  cloneable = FALSE,
  public = list(
    ssn = function() {
      date1 = as_date((date_time_init$new())$date_of_birth(maximum_age = 90))
      date2 = as_date("1899-12-31")
      diffdate = day(days(date1 - date2))

      digits = as.integer(str_split(diffdate, "")[[1]])
      digits = c(digits, replicate(4, private$random_int(0, 9)))

      weights = c(-1, 5, 7, 9, 4, 6, 10, 5, 7)
      checksum = sum(weights * digits)
      # Remainder of a checksum divided by 11 or 1 if it equals to 10

      digits = c(digits, checksum)
      return(str_c(digits, collapse = ""))
    }

  )
)
