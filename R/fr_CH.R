company_fr_CH = R6Class(
  "company_fr_CH",
  inherit = company_fr_FR,
  cloneable = FALSE,
  private = list(
    first_names = (person_fr_CH$new())$.__enclos_env__$private$first_names,
    last_names = (person_fr_CH$new())$.__enclos_env__$private$last_names,

    first_name = (person_fr_CH$new())$first_name,
    last_name = (person_fr_CH$new())$last_name,
    company_suffixes = c('SA', 'S\u00e0rl.')
  ),

  public = list(
    ide = function(){
      # Generates a IDE number (9 digits).
      # http://www.bfs.admin.ch/bfs/portal/fr/index/themen/00/05/blank/03/02.html

      control_number = 10
      while (control_number == 10){
        digits = sample(seq(0, 9), 8)
        factors = c(5, 4, 3, 2, 7, 6, 5, 4)
        sum_ = sum(digits * factors) %% 11
        control_number = 11 - sum_
        if (control_number != 10) {
          digits = c(digits, control_number)
        }
      }
      # finally return our random but valid BSN

      temp = c(
        str_c(digits[1:3], collapse = ""),
        str_c(digits[4:6], collapse = ""),
        str_c(digits[7:9], collapse = "")
      )

      return(str_c(str_c("CHE-", temp[1]),
                   temp[2], temp[3], sep = "."))
    },

    # uid: german name for ide
    uid = function() {
      return(self$ide())
    },
    # idi: italian name for ide
    idi = function() {
      return(self$ide())
    }
  )
)
