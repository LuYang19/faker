profile_fr_CH = R6Class(
  "profile_fr_CH",
  inherit = profile_init,
  cloneable = FALSE,
  public = list(
    simple_profile = function(sex) {
      # Generates a basic profile with personal informations
      SEX = c("F", "M")
      if (missing(sex)) {
        sex = sample(SEX, 1)
      }
      if (!(sex %in% SEX)) {
        sex = sample(SEX, 1)
      }

      name = ifelse(sex == "F",
                    (person_fr_CH$new())$name_female(),
                    (person_fr_CH$new())$name_male())
      temp = list(
        "username" = (internet_fr_CH$new())$user_name(),
        "name" = name,
        "sex" = sex,
        "address" = (address_fr_CH$new())$address(),
        "mail" = (internet_fr_CH$new())$free_email(),
        "birthdate" = (date_time_init$new())$date_of_birth()
      )
      return(temp)
    },

    profile = function(fields, sex) {
      # Generates a complete profile.
      # If "fields" is not empty, only the fields in the list will be returned

      if (missing(fields)) fields = c()

      field = list(
        "job" = (job_fr_CH$new())$job(),
        "company" = (company_fr_CH$new())$company(),
        "ssn" = (ssn_fr_CH$new())$ssn(),
        "residence" = (address_fr_CH$new())$address(),
        "current_location" = c((geo_init$new())$latitude(),
                               (geo_init$new())$longitude()),
        "blood_group" = sample(c(
          "A+", "A-", "B+", "B-", "AB+", "AB-", "O+", "O-"), 1),
        "website" = replicate(sample(seq(4), 1), (internet_fr_CH$new())$url())
      )

      field = append(field, self$simple_profile(sex))
      # field selection

      if (length(fields)) {
        nms = intersect(names(field), fields)
        field = field[nms]
      }
      return(field)
    }

  )
)













