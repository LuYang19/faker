ssn_fr_CH = R6Class(
  "ssn_fr_CH",
  inherit = ssn_init,
  cloneable = FALSE,
  private = list(
    ssn_formats = "###.####.####.##"
  ),
  public = list(
    ssn = function() {
      # Returns a 13 digits Swiss SSN named AHV (German) or
      # AVS (French and Italian)
      # See: http://www.bsv.admin.ch/themen/ahv/00011/02185/
      checksum = function(digits) {
        oddsum = sum(digits[seq(1, length(digits), 2)])
        evensum = sum(digits[seq(2, length(digits), 2)])
        return((10 - ((evensum + oddsum * 3) %% 10)) %% 10)
      }
      digits = c(7, 5, 6)
      # create an array of first 9 elements initialized randomly
      digits = c(digits, sample(seq(0, 9), 9))

      # determine the last digit to make it qualify the test
      digits = str_c(str_c(digits, collapse = ""), checksum(digits))
      ssn = str_c(str_sub(digits, 1, 3), ".", str_sub(digits, 4, 7), ".",
                  str_sub(digits, 8, 11), ".", str_sub(digits, 12, -1))
      return(ssn)
    },

    vat_id = function() {
      # :return: Swiss UID number
      checksum = function(digits) {
        digits = as.integer(str_split(digits, "")[[1]])
        code = c(8, 6, 4, 2, 3, 5, 9, 7)
        remainder = 11 - (sum(digits * code) %% 11)
        if (remainder == 10) return(0)
        if (remainder == 11) return(5)
        return(remainder)
      }

      vat_id = private$bothify('########')
      return(str_c("CHE", vat_id, checksum(vat_id)))
    }
  )

)

