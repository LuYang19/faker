ssn_he_IL = R6Class(
  "ssn_he_IL",
  inherit = ssn_init,
  cloneable = FALSE,
  public = list(
    ssn = function() {
      # https://en.wikipedia.org/wiki/Israeli_identity_card
      new_id = sprintf("%08d", round(runif(1, min = 111111, max = 99999999)))
      temp = 0
      index = c(1, 3, 5, 7)
      for (i in index) {
        num = as.integer(str_sub(new_id, i, i))
        temp = temp + num
        num = as.integer(str_sub(new_id, i + 1, i + 1)) * 2
        if (num > 9) {
          num = as.integer(str_sub(num, 1, 1)) +
            as.integer(str_sub(num, 2, 2))
        }
        temp = temp + num
      }
      last_digit = temp %% 10
      if (last_digit != 0) {
        last_digit = 10 - last_digit
      }

      return(str_c(new_id, last_digit))
    }
  )
)
