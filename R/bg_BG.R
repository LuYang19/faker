    simple_profile = function(sex) {
      # Generates a basic profile with personal informations
      SEX = c("F", "M")
      if (missing(sex)) {
        sex = sample(SEX, 1)
      }
      if (!(sex %in% SEX)) {
        sex = sample(SEX, 1)
      }

      name = ifelse(sex == "F",
                    (person_bg_BG$new())$name_female(),
                    (person_bg_BG$new())$name_male())
      temp = list(
        "username" = (internet_bg_BG$new())$user_name(),
        "name" = name,
        "sex" = sex,
        "address" = (address_bg_BG$new())$address(),
        "mail" = (internet_bg_BG$new())$free_email(),
        "birthdate" = (date_time_init$new())$date_of_birth()
      )
      return(temp)
    },

    profile = function(fields, sex) {
      # Generates a complete profile.
      # If "fields" is not empty, only the fields in the list will be returned

      if (missing(fields)) fields = c()

      field = list(
        "job" = (job_init$new())$job(),
        "company" = (company_bg_BG$new())$company(),
